export type CountryCode = keyof typeof countriesData

export const getCountryDepartments = (countryCodes: string[]): string[] => {
  const departments: string[] = []
  
  countryCodes.forEach(code => {
    const country = countriesData[code as CountryCode]
    if (country) {
      departments.push(...country.departments)
    }
  })
  
  return departments.sort()
}